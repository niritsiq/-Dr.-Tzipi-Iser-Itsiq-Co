function setLang(lang) {
  const dict = i18n[lang];
  document.documentElement.lang = dict.lang;
  document.documentElement.dir = dict.dir;

  // Query elements every time to support dynamically added content (like accessibility menu)
  const elements = document.querySelectorAll('[data-i18n]');

  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) {
      // Use innerHTML for fields that might contain HTML tags (lists, bold)
      if (key.includes('List') || key === 'heroSub' || key === 'aboutBody' || key.includes('ExpBody')) {
        el.innerHTML = dict[key];
      } else {
        el.textContent = dict[key];
      }
    }
  });

  if (btnHe && btnEn) {
    btnHe.classList.toggle('active', lang === 'he');
    btnEn.classList.toggle('active', lang === 'en');
  }

  // Update Accessibility Statement Link
  const a11yLink = document.getElementById('a11y-statement-link');
  if (a11yLink) {
    a11yLink.href = lang === 'he' ? 'accessibility-statement.html' : 'accessibility-statement-en.html';
  }
}

